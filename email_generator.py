def generate_supplier_email(company_name: str, inn: str, contact: str = '', product: str = '') -> str:
    """
    Генерирует шаблон письма поставщику для запроса КП или уточнения условий.
    """
    contact_line = f'Контакт для связи: {contact}\n' if contact else ''
    email = f"""
Здравствуйте!

Меня зовут [Ваше имя], я представляю организацию [Ваша компания].

Мы заинтересованы в сотрудничестве с вашей компанией {company_name} (ИНН: {inn}).

Просим направить коммерческое предложение на поставку:
{product or '[указать товар/услугу]'}

Если возможно, укажите:
— цену и условия поставки
— сроки
— минимальный объём заказа
— контактное лицо для связи

{contact_line}
Спасибо!
С уважением,
[Ваше имя]
[Ваша должность]
[Ваш телефон]
"""
    return email.strip() 